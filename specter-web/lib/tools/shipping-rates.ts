// Helpers
export function lookupRate(
  table: Record<Zone, number[]>,
  zone: Zone,
  weight_lb: number,
): number {
  const rates = table[zone]
  const billedLb = Math.max(1, Math.ceil(weight_lb)) // carriers round up to next lb

  const idx = WEIGHT_BREAKS.findIndex((w) => w >= billedLb)
  if (idx === -1) {
    // Above max — extrapolate linearly from last two points
    const n = WEIGHT_BREAKS.length
    const slope =
      (rates[n - 1] - rates[n - 2]) / (WEIGHT_BREAKS[n - 1] - WEIGHT_BREAKS[n - 2])
    return round2(rates[n - 1] + slope * (billedLb - WEIGHT_BREAKS[n - 1]))
  }
  return rates[idx]
}

export function calcDimWeightLb(
  length: number,
  width: number,
  height: number,
  divisor: number,
): number {
  return (length * width * height) / divisor
}

function round2(n: number) { return Math.round(n * 100) / 100 }
